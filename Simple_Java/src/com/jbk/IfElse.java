package com.jbk;

public class IfElse {
	public static void main(String[] args) {
		int x=100;
		int y=110;
		int z=133;
		if(x>y) {
			System.out.println(x+" is greater");
		}
		else if(y>z) {
			System.out.println(y+" is greater");
		}
		else {
			System.out.println(z+" is greater");
		}
	}

}
