package com.jbk;

public class TypeCasting {
	public static void main(Java_String[] args) {
		
		int num=10;
		double dbl=num; // Implicit casting/Widening casting/Automatic casting
		System.out.println(dbl);
		
		float f=345.85f;
		int i=(int)f; // Explicit casting/narrowing casting/mannual casting
		System.out.println(i);
		
		
		
		short s=1289;
		byte b=(byte)s;
		System.out.println(b);
		
		
	}

}
